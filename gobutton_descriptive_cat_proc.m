    'Title','A',...
    'Position',[.05 .78 .9 .2]);

h.cont_var1 = uicontrol(h.panel9 ,...
    'Style','text',...
    'String','Categorical Data',...
    'Units', 'normalized', ...
    'Position',[0.01 0.55 0.5 0.15]);

h.cont_var_val1= uicontrol(h.panel9 ,...
    'Style','pushbutton',...
    'String','Upload Data',...
    'Units', 'normalized', ...
    'Position',[0.45 0.55 0.25 0.2]);

