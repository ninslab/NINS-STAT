    'Style','edit',...
    'String',' ',...
    'Units', 'normalized', ...
    'Position',[0.755 0.55 0.15 0.2]);

h.group = uicontrol(h.panel9 ,...
    'Style','text',...
    'String','Grouping variable',...
    'Units', 'normalized', ...
    'Position',[0.01 0.25 0.5 0.15]);

h.group_var_upload = uicontrol(h.panel9 ,...
    'Style','pushbutton',...
    'String','Upload Data',...
    'Units', 'normalized', ...
    'Position',[0.45 0.25 0.25 0.2]);

h.group_var_edit = uicontrol(h.panel9 ,...
    'Style','edit',...
    'String',' ',...
    'Units', 'normalized', ...
    'Position',[0.755 0.25 0.15 0.2]);

