    'Style','pushbutton',...
    'String','Proceed',...
    'Units', 'normalized', ...
    'Position',[0.1 0.01 0.8 0.04]);

% Button Callbacks
%------------------
set(h.cont_var_val1, 'callback',{@cont_var_val1_proc, h});
